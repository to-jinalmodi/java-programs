package com.xcelerateDesign.javaPrograms.loops;

/**
 * Java for loop is divided into various parts as mentioned below
 *   -Initialization Expression
 *   -Test Expression
 *   -Update Expression
 */

public class Forloop {

    public static void main(String[] args) {

        for (int i = 0; i <= 100; i++) {
            System.out.print(i + " ");
        }

    }
}
