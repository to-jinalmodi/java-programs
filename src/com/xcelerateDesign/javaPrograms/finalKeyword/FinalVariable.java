package com.xcelerateDesign.javaPrograms.finalKeyword;

/**
 * Final variables: When a variable is declared as final,
 * its value cannot be changed once it has been initialized.
 * This is useful for declaring constants or other values that should not be modified.
 */
public class FinalVariable {

    public static void main(String[] args) {

        final double PI = 3.14159;

        // Print the value of PI
        System.out.println("Value of PI: " + PI);

    }

}
